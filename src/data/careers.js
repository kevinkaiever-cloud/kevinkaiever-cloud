function clamp(value, min, max) {
  return Math.max(min, Math.min(max, value));
}

function createCareer(name, category) {
  const real = CAREER_REAL_DATA[name];
  const base = real || CATEGORY_FALLBACK[category] || CATEGORY_FALLBACK["现代"];
  const dataSource = real?.src || (real ? "M" : "D");
  return {
    career_name: name,
    category,
    salary_2025: base.s / 1000,                          // K (thousands yuan/month)
    risk_level: clamp(base.r, 1, 10),
    growth_score: clamp(base.g, 1, 100),
    ai_replace_risk: clamp(base.a, 1, 100),
    stability_score: clamp(base.t, 1, 100),
    dataSource,
    samples: real?.samples,
  };
}

function uniqueNames(groups) {
  const seen = new Set();
  const unique = {};
  Object.entries(groups).forEach(([category, list]) => {
    unique[category] = [];
    list.forEach((name) => {
      if (!seen.has(name)) {
        unique[category].push(name);
        seen.add(name);
      }
    });
  });
  return unique;
}

const UNIQUE_GROUPS = uniqueNames(CAREER_GROUPS);

const CAREER_DB = Object.entries(UNIQUE_GROUPS)
  .flatMap(([category, list]) => list.map((name) => createCareer(name, category)));

const CAREER_MAP = CAREER_DB.reduce((acc, item) => {
  acc[item.career_name] = item;
  return acc;
}, {});

